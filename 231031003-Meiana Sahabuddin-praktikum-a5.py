#Menginisialisasi sebuah variabel boolean 'a' dengan nilai 'True','count=0': Menginisialisasi sebuah variabel penghitung 'count' dengan nilai '0'
a = True
count = 0

#While a Memulai perulangan sementara yang terus berlanjut selama kondisi `a` bernilai benar.`print('menjalankan program')`: Mencetak pesan yang ditentukan.
while a:
        print('Menjalankan Program')
        count += 1
        if count == 5:
            break

#`count += 1`: Menambah penghitung sebanyak 1 di setiap iterasi.
# `jika hitungan == 5: istirahat`: Memeriksa apakah penghitung telah mencapai 5 dan keluar dari perulangan jika benar.
